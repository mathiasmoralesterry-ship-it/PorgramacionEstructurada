#Lista de Requirimientos de una Veterinaria
#1-El sistema debe permitir registrar nuevos clientes.
#2-El sistema debe permitir registrar mascotas asociadas a un cliente.
#3-El sistema debe generar un código único para cada mascota.
#4-El sistema debe permitir editar los datos del cliente.
#5-El sistema debe permitir editar la información de las mascotas.
#6-El sistema debe eliminar registros de clientes cuando sea autorizado.
#7-El sistema debe eliminar registros de mascotas cuando sea necesario.
#8-El sistema debe buscar clientes por nombre o DNI.
#9-El sistema debe buscar mascotas por nombre, especie o código.
#10-El sistema debe registrar citas veterinarias.
#11-El sistema debe modificar la fecha y hora de una cita.
#12-El sistema debe cancelar citas programadas.
#13-El sistema debe mostrar el calendario de citas del día.
#14-El sistema debe registrar la atención médica realizada.
#15-El sistema debe almacenar el historial clínico de cada mascota.
#16-El sistema debe registrar vacunas aplicadas.
#17-El sistema debe registrar tratamientos médicos.
#18-El sistema debe registrar recetas emitidas por el veterinario.
#19-El sistema debe controlar el stock de medicamentos.
#20-El sistema debe registrar la venta de medicamentos y productos.
#21-El sistema debe emitir comprobantes o boletas de pago.
#22-El sistema debe calcular automáticamente el monto total de la atención.
#23-El sistema debe registrar diferentes métodos de pago (efectivo, tarjeta, transferencia, etc.).
#24-El sistema debe registrar los datos de los veterinarios.
#25-El sistema debe asignar un veterinario a cada cita.
#26-El sistema debe generar reportes de ingresos diarios, semanales y mensuales.
#27-El sistema debe generar reportes de mascotas atendidas.
#28-El sistema debe mostrar alertas de próximas vacunas o controles médicos.
#29-El sistema debe permitir el inicio de sesión con usuario y contraseña.
#30-El sistema debe cerrar sesión de forma segura y proteger la información almacenada.